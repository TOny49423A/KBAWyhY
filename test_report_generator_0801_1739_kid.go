package main

import (
    "fmt"
    "net/http"
    "os"
    "text/template"

    "github.com/labstack/echo"
)

// Define the TestReport structure
type TestReport struct {
    Name        string
    Description string
    Results     string
}

func main() {
    // Initialize the Echo instance
    e := echo.New()

    // Set up a route for generating test reports
    e.GET("/report", generateTestReport)

    // Start the server
    e.Logger.Fatal(e.Start(":8080"))
}

// generateTestReport is a handler function that generates a test report
func generateTestReport(c echo.Context) error {
    // Create a new TestReport instance
    report := TestReport{
        Name:        "Test Report",
        Description: "This is a test report generated by the TestReportGenerator program",
        Results:     "All tests passed successfully",
    }

    // Create a template for the report
    tmpl, err := template.New("report").Parse(`
        <html>
        <head>
            <title>{{.Name}}</title>
        </head>
        <body>
            <h1>{{.Name}}</h1>
            <p>{{.Description}}</p>
            <p>{{.Results}}</p>
        </body>
        </html>
    `)
    if err != nil {
        // Handle template parsing error
        return err
    }

    // Execute the template with the report data
    if err := tmpl.Execute(os.Stdout, report); err != nil {
        // Handle template execution error
        return err
    }

    // Return the rendered template as HTML
    return c.HTML(http.StatusOK, tmpl.ExecuteTemplate(os.Stdout, "report", report).String())
}
